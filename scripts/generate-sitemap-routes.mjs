import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { flatRoutes } from "@remix-run/dev/dist/config/flat-routes.js";

const currentDirectory = path.dirname(fileURLToPath(import.meta.url));
const rootDirectory = path.resolve(currentDirectory, "..");
const appDirectory = path.join(rootDirectory, "app");
const outputFile = path.join(appDirectory, "generated", "sitemap-routes.ts");

const EXCLUDED_PATHS = new Set(["/robots.txt", "/sitemap.xml"]);
const EXCLUDED_PATH_PREFIXES = ["/admin", "/wallet"];

const routeManifest = flatRoutes(appDirectory);
const pathnameCache = new Map([["root", "/"]]);

const normalizePathname = (pathname) => {
  if (!pathname) {
    return "/";
  }

  const normalizedPathname = pathname.replace(/\/+/g, "/");

  if (normalizedPathname === "/") {
    return normalizedPathname;
  }

  return normalizedPathname.endsWith("/")
    ? normalizedPathname.slice(0, -1)
    : normalizedPathname;
};

const joinPathnames = (parentPathname, childPathname) => {
  if (!childPathname) {
    return normalizePathname(parentPathname);
  }

  return normalizePathname(`${parentPathname}/${childPathname}`);
};

const getRoutePathname = (routeId) => {
  const cachedPathname = pathnameCache.get(routeId);

  if (cachedPathname) {
    return cachedPathname;
  }

  const route = routeManifest[routeId];

  if (!route) {
    throw new Error(`Unknown Remix route: ${routeId}`);
  }

  const parentPathname = getRoutePathname(route.parentId ?? "root");
  const pathname = joinPathnames(parentPathname, route.path ?? "");

  pathnameCache.set(routeId, pathname);

  return pathname;
};

const isStaticPathname = (pathname) =>
  !pathname.includes(":") && !pathname.includes("*");

const isExcludedPathname = (pathname) =>
  EXCLUDED_PATHS.has(pathname) ||
  EXCLUDED_PATH_PREFIXES.some(
    (prefix) => pathname === prefix || pathname.startsWith(`${prefix}/`)
  );

const sortPathnames = (firstPathname, secondPathname) => {
  if (firstPathname === secondPathname) {
    return 0;
  }

  if (firstPathname === "/") {
    return -1;
  }

  if (secondPathname === "/") {
    return 1;
  }

  return firstPathname.localeCompare(secondPathname);
};

const staticPathnames = [...new Set(Object.keys(routeManifest).map(getRoutePathname))]
  .filter(isStaticPathname)
  .filter((pathname) => !isExcludedPathname(pathname))
  .sort(sortPathnames);

const fileContents = `/**
 * This file is auto-generated by \`npm run generate:sitemap-routes\`.
 * Do not edit it manually.
 */

export const GENERATED_STATIC_SITEMAP_PATHS = [
${staticPathnames.map((pathname) => `  "${pathname}",`).join("\n")}
] as const;
`;

await mkdir(path.dirname(outputFile), { recursive: true });
await writeFile(outputFile, fileContents, "utf8");

console.log(
  `Generated ${path.relative(rootDirectory, outputFile)} with ${staticPathnames.length} static routes.`
);
